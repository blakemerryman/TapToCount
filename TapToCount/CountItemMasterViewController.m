#pragma mark - Table view data source
- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 0;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return [self.dataController countOfList];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"CountItemCell";
    
    // Creates a new UITableViewCell instance...
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    
    // Gets the CountItem object from index path.
    CountItem *itemAtIndex = [self.dataController objectInListAtIndex:indexPath.row];
    
    // Sets the primary text label to be the item's name.
    [[cell textLabel] setText:itemAtIndex.itemName];
    
    // Converts the item count to string for display.
    NSString* countAsString = [NSString stringWithFormat:@"%lu", (unsigned long)itemAtIndex.itemCount];
    
    // Sets detail text to item's count value.
    [[cell detailTextLabel] setText:countAsString];
    
    return cell;
}

